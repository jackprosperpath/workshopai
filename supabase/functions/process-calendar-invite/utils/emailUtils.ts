/**
 * Send a confirmation email to the workshop organizer with a link to the generated blueprint
 */
export async function sendConfirmationEmail(
  resend: Resend,
  email: string,
  eventSummary: string, // Original event summary
  eventDescription: string, // Original event description
  blueprintShareUrl: string, // Changed from workshopUrl
  blueprint: Blueprint | null // The generated blueprint
): Promise<void> {
  try {
    // Format the blueprint preview for the email
    const blueprintPreview = blueprint ? {
      title: blueprint.title || eventSummary, // Fallback to event summary if blueprint title is missing
      totalDuration: blueprint.totalDuration,
      steps: blueprint.steps ? blueprint.steps.slice(0, 3) : [], // Get first 3 steps only, ensure steps exist
      materials: blueprint.materials || [] // Ensure materials exist
    } : null;

    console.log("Blueprint Share URL:", blueprintShareUrl);
    console.log("Original event description length:", eventDescription.length);
    console.log("Blueprint for email preview available:", !!blueprintPreview);
    console.log("Preparing to send blueprint confirmation email to:", email);

    // Send the email
    const { data, error } = await resend.emails.send({
      from: "Teho.ai Blueprint Assistant <agenda@teho.ai>", // Updated sender name
      to: email,
      subject: `Your Teho.ai Blueprint: ${eventSummary}`, // Updated subject
      html: agendaEmail({
        hostName: email.split('@')[0],
        agendaPreview: eventDescription, // Original meeting description
        editorUrl: blueprintShareUrl,    // Link to view the blueprint
        blueprintPreview                 // The structured preview of the blueprint
      })
    });

    console.log("Email send result:", JSON.stringify({ data, error }));

    if (error) {
      throw error;
    }

    console.log("Blueprint confirmation email sent to:", email);
  } catch (error) {
    console.error("Failed to send blueprint confirmation email:", error);
    throw new Error(`Failed to send blueprint confirmation email: ${error.message}`);
  }
}

